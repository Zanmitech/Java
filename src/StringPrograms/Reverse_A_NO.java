package StringPrograms;

import java.util.Scanner;

public class Reverse_A_NO {

	public static void main(String[] args) {
		int n,rem;
		Scanner sc = new Scanner(System.in);
		System.out.println("Enter a num");
		n= sc.nextInt();
		while(n!=0){
			rem=n%10;
			System.out.print(rem);
			n=n/10;
			

	}

}}
